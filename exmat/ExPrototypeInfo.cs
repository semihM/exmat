namespace ExMat.FuncPrototype
{
    /// <summary>
    /// Prototype information
    /// </summary>
    public struct ExPrototypeInfo
    {
        /// <summary>
        /// Number of instructions
        /// </summary>
        public int nInstr;
        /// <summary>
        /// Number of literals
        /// </summary>
        public int nLits;
        /// <summary>
        /// Number of parameters
        /// </summary>
        public int nParams;
        /// <summary>
        /// Number of functions
        /// </summary>
        public int nFuncs;
        /// <summary>
        /// Number of outers
        /// </summary>
        public int nOuters;
        /// <summary>
        /// Number of line infos
        /// </summary>
        public int nLineInfos;
        /// <summary>
        /// Number of local var infos
        /// </summary>
        public int nLocalInfos;
        /// <summary>
        /// Number of default values
        /// </summary>
        public int nDefaultParameters;
    }
}
